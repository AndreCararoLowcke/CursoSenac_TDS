
package uc9ativ3a;

public class ListaCadastro {
    private String nome;
    private String telefone;
    private String cpf;
    private String dataconsulta;
    private String ja;
    
    public void ListaCadastro(String nome, String telefone, String cpf, String dataconsulta, String ja, String realizada){
    
        this.setNome(nome);
        this.setNome(telefone);
        this.setNome(cpf);
        this.setNome(dataconsulta);
        this.setNome(ja);
        this.setNome(realizada);
    
    }
}